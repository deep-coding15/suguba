export const currencies = {
  fcfa: { label: "Fcfa", rate: 1, symbol: "Fcfa" },
  eur: { label: "Euro (€)", rate: 0.00152, symbol: "€" },
  usd: { label: "Dollar ($)", rate: 0.00165, symbol: "$" },
  mad: { label: "Dirham (DH)", rate: 0.0165, symbol: "DH" },
};

export function formatPrice(priceFcfa, currency) {
  const cur = currencies[currency] || currencies.fcfa;
  const converted = (priceFcfa * cur.rate).toFixed(currency === "fcfa" ? 0 : 2);
  return `${parseFloat(converted).toLocaleString()} ${cur.symbol}`;
}




